package JavaProgrammingIntro.ch04;

public class ch_4_0_arith {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 3;

        System.out.println("num1 + num2 = " + (num1 + num2));
        System.out.println("num1 - num2 = " + (num1 - num2));
        System.out.println("num1 * num2 = " + (num1 * num2));
        System.out.println("num1 / num2 = " + (num1 / num2)); // 나누기 연산. 결과 2 출력
        System.out.println("num1 % num2 = " + (num1 % num2)); // 나머지 연산. 결과 1 출력
    }
}
